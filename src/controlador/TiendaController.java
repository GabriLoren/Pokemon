package controlador;

import javafx.fxml.FXML;
import javafx.fxml.Initializable;
import javafx.scene.control.Button;

import javafx.scene.control.TextField;
import modelo.Obj;
import modelo.Trainer;

import java.net.URL;
import java.util.LinkedList;
import java.util.ResourceBundle;

import cargar.CargarEntrenador;
import cargar.CargarTodosLosObjetos;
import funcionalidades.AddObject;
import funcionalidades.ComprarObjeto;
import javafx.event.ActionEvent;

import javafx.scene.control.Label;

public class TiendaController implements Initializable {
	@FXML
	private Button btnComprar;
	@FXML
	private TextField txtObjetoComprar;
	@FXML
	private  Label lblMisObjetos;
	@FXML
	private Label lblObjetosTienda;
	
	private static Trainer entrenador=CargarEntrenador.getEntrenador();
	
	
	private static String mostrarObjetosTienda;
	
	private static LinkedList<Obj>objetosTienda=CargarTodosLosObjetos.getTodosLosObjetos();
	


	// Event Listener on Button[#btnComprar].onAction
	@FXML
	public void comprar(ActionEvent event) {
		// TODO Autogenerated
		
		String objetoComprado=txtObjetoComprar.getText();
		

		for(int i=0;i<objetosTienda.size();i++) {
			
			Obj objetoElegido=objetosTienda.get(i);
			
			System.out.println(objetosTienda.get(i).getName());
			
			if(objetoComprado.equalsIgnoreCase(objetoElegido.getName())) {
				
				System.out.println("ha entrado");
				
				entrenador.getObjetos().add(objetoElegido);
				
//				AddObject.insertarEnBbDdElObjetoComprado(objetoElegido, entrenador.getId());
				
				ComprarObjeto.comprarObjeto(entrenador, objetoElegido);
				
				
				lblMisObjetos.setText(AddObject.mostraObjetosEntrenador(entrenador.getObjetos()));
				
				break;
				
			}else {
				
				lblMisObjetos.setText("Ese objeto no existe\n"+AddObject.mostraObjetosEntrenador(entrenador.getObjetos()));
				
			}
		}
		
	}

	@Override
	public void initialize(URL arg0, ResourceBundle arg1) {
		// TODO Auto-generated method stub
		
		if(entrenador.getObjetos().size()>0) {
			lblMisObjetos.setText(AddObject.mostraObjetosEntrenador(entrenador.getObjetos()));
		}
		else lblMisObjetos.setText("Mochila vacia");
		
		
		
		lblObjetosTienda.setText(objetosTienda.toString());

	}
}
